package ConstantPool;
// The area where these immutable objects have been created that area is called is String constant pool
// Write a program to check whether string starts with a and ends with z. Refer below example
public class A3 {
	public static void main(String[] args) {
		String str = "ansmsjdhbeevcz";
	System.out.println(str.startsWith("a"));
	System.out.println(str.endsWith("z"));
	System.out.println(str.startsWith("x"));
	System.out.println(str.endsWith("x"));
	}

}
